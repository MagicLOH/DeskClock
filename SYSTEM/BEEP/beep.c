#include "beep.h"

/*
***********************************************************
    
    函数原型：
    函数功能：初始化蜂鸣器工作模式[默认低电平触发]
    函数形参：无
    硬件连接：BEEP -- PA6;
    返回值：无
    
************************************************************ 
*/
void Beep_Init(void )
{
    RCC->APB2ENR |= 1<<2;//GPIOA 时钟使能
    
    GPIOA->CRL &= 0xF0FFFFFF;
    GPIOA->CRL |= 0x03000000;
}


